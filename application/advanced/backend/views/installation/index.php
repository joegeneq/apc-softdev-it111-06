<h3>How to Install NVDA </h3>

<p>1. Click the Skip Donation to download the NVDA software directly, and wait until it's finished. </p>
	<img src="images/installation/ht1.png" alt="Image of NVDA Download"></img>
<p>2. Run the NVDA software to start installation </p>
	<img src="images/installation/ht2.png" alt="Image of NVDA Installation"></img>
<p>3. Wait until it finishes installation </p>
<p>4. After installing, double click the shortcut icon to launch NVDA Screen Reader. </p>
	<img src="images/installation/ht3.png" width="110px" height="120px" alt="Image of NVDA Shortcut key"></img>
<h3>How to Install Sound on click extension </h3>
<p>1. Open Google Chrome and go to the Toolbar located on the right side </p>
<p>2. Go to Settings > Extensions (left side) </p>
<p>3. Click Get More extensions (Bottom) </p>
	<img src="images/installation/ht4.png" alt="Image of Get More Extensions button"></img>
<br><br>
<p>4. Type "sound click" on the Search the store bar </p>
	<img src="images/installation/ht5.png" width="700px" height="400px" alt="Image showing the search bar in Google Store"></img>
<br><br>	
<p>5. Search for Sound Click in Extensions </p>
	
<p>6. Download / Install extension by clicking Free </p>
<img src="images/installation/ht6.png" width="700px" height="400px" alt="Image of Extensions"></img>
<br><br>
<p>7. Go to back to Extensions page in the Settings and Enable the sound on click extension </p>
