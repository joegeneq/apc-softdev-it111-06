<h1>Help</h1>

<h3> General Access Keys: </h3>
<p class="installation">
  1. Press Alt + C to Go to Customer Page.<br />
  2. Press Alt + S to Go to Supplier Page.<br />
  3. Press Alt + I to Go to Product Inventory Page.<br />
  4. Press Alt + O to Go to Order Page. <br />
  5. Press Alt + P to Go to Purchases Page.<br />
  6. Press Alt + L to Go to Help Page.<br />
  7. Press Alt + H to Go to Home Page.<br />
</p>

<h3> On Page Access Keys: </h3>
<p class="installation">
  1. Press Alt + A to Add Customers, Orders, Suppliers, Purchases and Product.<br />
  2. Press Alt + M to go to Manage Customer, Orders, Suppliers, Purchases and Product.<br />
  3. Press Alt + B or press the Backspace button to Go Back to previous page.<br />
</p> 

